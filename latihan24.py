# convert the decimal number to be binary, octa and hexadecimal
# input the data 
dec_num = int(input("Enter the decimal number : "))


# convert using library oct, hexadecimal and binary
print ("Decimal value", dec_num, "is")
print(bin(dec_num), "in binary")
print (oct(dec_num), "in octa")
print (hex(dec_num), "in hexadecimal")